				<h2 class="divHeader">
					Academic Talent Development Program
				</h2>
				<p>
					Academic Talent Development Program (ATDP) of UC Berkeley, administrated by the Graduate School of Education, offers challenging summer classes to highly motivated young scholars and serves as a college preparation program for Grade 7 to 12 high school students. ATDP is officially integrated into the UC online college application platform and students' attending records are recognized as their additional academic history. In tandem with Berkeley's educational mission, ATDP embraces social development as a complement to the traditional academic learning by incorporating community service and outreach projects in its Global Programs, which aims to imbue global awareness and environmental consciousness in the students.
				</p>
				<a href="https://atdp.berkeley.edu/" target="_blank">Learn more about ATDP <i class="fa fa-external-link" aria-hidden="true"></i></a>
				<h3 class="divHeader marginTop">
					ATDP Global Programs
				</h3>
				<p>
					ATDP Global Programs is a 4-week intensive summer program that is also packed with fun weekend activities and an atmosphere of camaraderie created by Berkeley's talented and versatile college students as both residential and academic counselors. Student alumni have expressed a strong sense of accomplishment and attachment to Berkeley. Upon their children's return home, parents have also observed them becoming more independent and self-driven with a clearer academic direction.
				</p>
				<p>
					CFM Educational Services is the official ATDP partner that administrates the residential program as well as the community service and outreach projects. ATDP students, domestic or international, who have residential needs are supported under the Global Programs. Berkeley ATDP and CFM also work together on Global Programs promotion and international recruitment.
				</p>
				<a href="https://atdp.berkeley.edu/gp/" target="_blank">Learn more about ATDP Global Programs <i class="fa fa-external-link" aria-hidden="true"></i></a>
